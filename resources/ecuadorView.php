							<center><strong>DERECHOS</strong></center>
							<hr />
							<ul>
								<li>Todo paciente tiene derecho a ser atendido oportunamente en el centro de salud de acuerdo a la dignidad que merece todo ser humano y tratado con respeto, esmero y cortesía.</li>
								<li>Todo paciente tiene derecho a no ser discriminado por razones de sexo, raza, edad, religión o condición social y económica.</li>
								<li>Todo paciente tiene derecho a que la consulta, examen, diagnóstico, discusión, tratamiento y cualquier tipo de información relacionada con el procedimiento médico a aplicársele, tenga el carácter de confidencial.</li>
								<li>Se reconoce el derecho de todo paciente a que, antes y en las diversas etapas de atención al paciente, reciba del centro de salud a través de sus miembros responsables, la información concerniente al diagnóstico de su estado de salud, al pronóstico, al tratamiento, a los riesgos a los que médicamente está expuesto, a la duración probable de incapacitación y a las alternativas para el cuidado y tratamientos existentes, en términos que el paciente pueda razonablemente entender y estar habilitado para tomar una decisión sobre el procedimiento a seguirse. Exceptúense las situaciones de emergencia.</li>
								<li>El paciente tiene derecho a que el centro de salud le informe quien es el médico responsable de su tratamiento.</li>
								<li>Todo paciente tiene derecho a elegir si acepta o declina el tratamiento médico. En ambas circunstancias el centro de salud deberá informarle sobre las consecuencias de su decisión.</li>
							</ul>
						</div>
						<div class="mt-2">
							<hr />
				  			<center><h4>DEFENSA JURÍDICA DE LOS DERECHOS DE LOS PACIENTES</h4></center>
							<hr />
							<div class="justify-content">
								A diferencia de Colombia, que el trámite se realiza por medio de acción de tutela, en Ecuador se realiza a través de la Defensoría del Pueblo donde, con el fin de tutelar el derecho a la salud en determinadas situaciones, emiten resoluciones defensoriales donde exhortan a ciertas entidades a garantizar el derecho objeto de priorización.<br />
								Con este tipo de acciones, se "busca precautelar el bienestar de las personas de atención prioritaria, dejando un precedente para casos similares que puedan presentarse en el futuro." (Defensoría del Pueblo, 2016).
