						<center><h4>DEFINICIÓN</h4></center>
						<hr />
						<div class="justify-content">
							Este sistema de salud se caracteriza principalmente por la segmentación, es decir, "la coexistencia de subsistemas con distintas modalidades de financiamiento, afiliación y provisión de servicios a diferentes grupos de la población", segmentación que surge de la evolución de cada uno de los subsistemas.
						</div>
					</div>
					<div class="tab-pane fade" id="organisms" role="tabpanel" aria-labelledby="profile-tab">
						<div class="mt-4">
							<center><strong>MINISTERIO DE SALUD PÚBLICA (MSP)</strong></center>
							<hr />
							<center><img src="../public/media/msp.jpeg" width="300" height="300"></center>
							<div class="justify-content">
								Es la cartera del Estado ecuatoriano que tiene como funciones de ejercer la rectoría, regulación, planificación, coordinación, control y gestión de la salud pública.
							</div>
							<hr />
							<center><h4>ORGANIGRAMA DEL MINISTERIO DE SALUD PUBLICA</h4></center>
							<hr />
							<div class="mt-2">
								<embed src="../public/media/organigrama.pdf" type="application/pdf" width="100%" height="600px" />
