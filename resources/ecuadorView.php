					<div class="tab-pane fade" id="segmentation" role="tabpanel" aria-labelledby="profile-tab">
						<div class="row mt-4">
							<center><strong>​​​​​​​SEGMENTACIÓN DEL SISTEMA DE SALUD</strong></center>
							<hr />
							<div class="col-md-6">
								<center><strong>​​​​​​​SECTOR PÚBLICO</strong></center>
								<ul>
									<li>ENTIDADES:
										<ul>
											<li>Prestadores de salud provenientes del gobierno:
												<ol>
													<li>Ministerio de Salud Pública (MPS).</li>
													<li>Ministerio de Inclusión Económica y Social (MIES).</li>
													<li>Municipalidades.</li>
												</ol>
											</li>
											<li>Prestadores de salud provenientes de las contribuciones de los trabajadores:
												<ol>
													<li>Instituto de Seguridad Social de las Fuerzas Armadas (ISSFA)</li>
													<li>Instituto de Seguridad Social de la Policía Nacional (ISSPOL)</li>
												</ol>
											</li>
											<li>Prestadores de salud provenientes de las contribuciones de los empleadores: 
												<ol>
													<li>Instituto Ecuatoriano de Seguridad Social (IESS)</li>
												</ol>
											</li>
										</ul>
									</li>
								</ul>
								<hr />
								<div class="justify-content">
									El sector público está conformado por el MSP, el MIES, los servicios de salud de las municipalidades y las IESS.

									El primero de ellos ofrece servicios de salud a toda la población ecuatoriana. Con respecto a el MIES y las municipalidades, estas entidades cuentan con programas y establecimientos en los que brindan atención a la población que no se encuentra asegurada y, finalmente, las IESS cubren a la población asalariada que sí cuenta con seguro.
								</div>
							</div>
							<div class="col-md-6">
								<center><strong>​​​​​​​SECTOR PRIVADO</strong></center>
								<ul>
									<li>ENTIDADES:
										<ul>
											<li>Prestadores de salud provenientes de contribuciones personales, patronales y externas:
												<ol>
													<li>Seguros privados.</li>
												</ol>
											</li>
										</ul>
									</li>
								</ul>
								<hr />
								<div class="justify-content">
									El sector privado comprende entidades con fines de lucro, así como organizaciones sin fines de lucro de servicio social. Adicionalmente, se encuentran los seguros de carácter privado y las empresas de medicina prepagada, quienes cubre aproximadamente al 3% de la población de ingresos medios y altos, así como se encuentran, al menos, 10.000 consultorios médicos particulares ubicados en las principales ciudades y en los que el pago por la prestación del servicio suele hacerse directamente al momento de recibir la atención. (Aquiles, R. Sistema de salud de Ecuador. 2010).
								</div>
							</div>
						</div>
						<hr />
						<center><h4>FINANCIAMIENTO</h4></center>
						<hr />
						<div class="justify-content">
							Constitución Política de la República de Ecuador - Artículo 46: "El financiamiento de las entidades públicas del sistema nacional de salud provendrá de aportes obligatorios, suficientes y oportunos del Presupuesto General del Estado, de personas que ocupen sus servicios y que tengan capacidad de contribución económica y de otras fuentes que señale la ley."
						</div>
						<div class="row mt-2">
							<div class="col-md-6">
								Acorde con la Ley de Seguridad Social promulgada en el año 2001, el Seguro General Obligatorio del IESS tiene la siguientes fuentes de financiación:
								<ul>
									<li>La aportación individual de los afiliados.</li>
									<li>La aportación patronal de los empleadores, privados y públicos.</li>
									<li>La contribución del Estado.</li>
								</ul>
							</div>
							<div class="col-md-6">
								Con respecto al ISSPOL y la ISSFA, los recursos provienen del aporte de los miembros de la policía y de los militares activos, respectivamente, así como los aportes que hace el Ministerio de Defensa, en el caso de los militares, y del Estado, en el caso de la policía.
							</div>
