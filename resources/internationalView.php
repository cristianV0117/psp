					<div class="row">
						<div class="col-md-6">
							<center><strong>DEFINICIÓN</strong></center>
							<hr />
							La Corte Interamericana es uno de los tres tribunales regionales de protección de los derechos humanos. Es una institución judicial cuyo objetivo es aplicar e interpretar la Convención Americana. La Corte Interamericana ejerce una función contenciosa, dentro de la que se encuentra la resolución de casos contenciosos y el mecanismo de supervisión de sentencias; una función consultiva; y la función de dictar medidas provisionales.
						</div>
						<div class="col-md-6">
							<center><strong>FUNCIONES</strong></center>
							<hr />
							<ul>
								<li>
									Contenciosa: La Corte determina si un Estado ha incurrido en responsabilidad internacional por la violación de alguno de los derechos consagrados en la Convención Americana o en otros tratados de derechos humanos aplicables, así como la supervisión de cumplimiento de sentencias.
								</li>
								<li>
									Medidas provisionales: Son medidas que se dictan en casos de extrema gravedad y urgencia, y cuando se haga necesario evitar daños irreparables a las personas
								</li>
								<li>
									Consulta: La Corte responde consultas que formulan los Estados miembros de la OEA o los órganos de la misma acerca de la compatibilidad de las normas internas con la Convención y la interpretación de la Convención o de otros tratados concernientes a la protección de los derechos humanos.
								</li>
							</ul>
							<center><strong>DERECHOS CONSAGRADOS EN LA CONVENCIÓN AMERICANA</strong></center>
							<ul>
								<li>Reconocimiento de la personalidad jurídica.</li>
								<li>Derecho a la vida.</li>
								<li>Derecho a la integridad personal.</li>
								<li>Prohibición de la esclavitud y la servidumbre.</li>
							</ul>
